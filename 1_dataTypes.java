class DataType{
    public static void main(String[] args) {
        byte a = 6; // 1 byte  
        short b = 6; // 2 bytes  
        int c = 6383; // 4 bytes  
        long d = 6334l; // 8 bytes  

        boolean b1 = true; // true or false no (1,0)

        char c1 = 'a';

        // by default any floating point is assigned with double data type
        float f1 = 23.3f; // 4 bytes
        double d1 = 2224.2232; // 8 bytes

        System.out.println("Data types");
    }
}