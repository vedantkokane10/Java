class RelationalOperator {
    public static void main(String[] args) {
        int a = 4, b = 5;
        boolean result = a <= b;
        System.out.println(result);
        boolean result1 = a >= b;
        System.out.println(result1);

        int a1 = 3, b1 = 3;
        boolean result2 = a1 == b1;
        System.out.println(result2);
    }
}
